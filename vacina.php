            <h1>O que são as vacinas?</h1>

            <p>As vacinas são um metodo de prevenção contra doenças, que ajudam nosso sistema imunologico a se preparar
                e se proteger contra futuros ataques. As vacinas produzidas durante a pandemia da COVID-19, utilizam
                diferentes metodos que trabalham em nosso organismo, para desenvolver barreiras, fazendo com que os
                sintomas da doença, caso contraída, sejam menores e não agressivos. As vacinas nos protegem de forma
                segura e eficaz e permitem que nosso sistema imunológico esteja preparado para combater determinadas
                infecções, caso ocorram.</p>

            <h3>O que são as vacinas contra a COVID-19?</h3>

            <p>Cada vacina é produzida com uma tecnologia para produção de anticorpos. Todas as vacinas são seguras e
                eficazes. Independente da tecnologia utilizada, esses imunizantes foram testados, tiveram sua eficácia
                comprovada por meio de estudos, avaliados e reavaliados por cientistas, e são aplicados em larga escala
                no mundo todo.</p>

            <h3>A vacina do COVID-19 e os negacionistas</h3>

            <p>A justificativa sobre não tomar vacinas pode variar entre as reações mais leves à perda de neuronios. Os
                negacionistas ou anti-vac tem a certeza que todas as vacinas são apenas uma maneira de prejudicar a
                saúde da população em massa, principalmente a vacina contra o COVID-19. Pesquisas ja comprovaram a
                eficacia das vacinas, mas mesmo com provas apresentadas, os negacionistas afirmam que essas provas são
                feitas para enganar a população.</p>

            <p>O movimento negacionista trata-se de um movimento organizado, que se beneficia do cenário de confusão e
                ansiedade alimentado pelos boatos. São pessoas que colocam em dúvida não apenas a segurança e a eficácia
                das vacinas, mas também de outras rotinas de saúde pública comprovadamente benéficas como, por exemplo,
                o flúor na água.</p>

            <p>Mesmo com tantos absurdos sendo ditos, ainda temos pessoas que acreditam neste absurdo. Para que não
                acreditem nisso e fake news não sejam disseminadas, temos evidências que indicam que pessoas totalmente
                vacinadas têm menos probabilidade de infecção, incluindo assintomática, de adoecer de forma grave e de
                transmitir o vírus a outras pessoas. O grau de proteção, entretanto, pode variar de acordo com a
                resposta imunológica de cada indivíduo, das variantes do vírus em circulação e do tempo decorrido desde
                a vacinação.</p>
            <p>É importante ressaltar que completar o esquema é essencial para obter a proteção máxima contra a
                enfermidade. Além disso, com um grande percentual da população adequadamente vacinado, podemos reduzir a
                circulação do vírus, as chances de infecção das pessoas que não respondem bem às vacinas ou não podem
                ser vacinadas e dificultar o surgimento de variantes mais agressivas.</p>

