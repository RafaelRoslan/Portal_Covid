                <h4>Coronavac</h4>

                    <p>Vacina criada com uma tecnologia molecular, assim como as vacinas da gripe, poliomielite, hepatite e meningite, é feita por um vírus inativo ou vírus "morto", onde apenas a parte responsavel pelo reconhecimento do vírus é injetada para que o nosso organismo crie a imunidade contra ele, não permitindo que se multiplique e ataque o organismo.</p>
                    <p>Assim que a vacina for aplicada, células de defesa do nosso organismo encontram e respondem a essas partes do coronavírus, dando início à produção de anticorpos.</p>
                    <p>O esquema de vacinação é com uma dose de reforço depois de 4 semanas, para reforço nos anticorpos produzidos.As reações desta vacina são mais leves, sendo dor no local, dores de cabeça ou não ter reação alguma.</p>
                    <p>A eficácia geral apresentada pelo Instituto Butantan para a CoronaVac nos testes brasileiros foi de 50,38%, o que pode parecer baixo em primeiro momento, mas que traz ótimos resultados quando detalhados: a vacina mostrou-se 100% eficaz nos casos moderados e graves e 78% eficaz nos casos leves da covid-19. Ou seja, a aplicação da vacina, quando feita adequadamente em duas doses, tem grande potencial de redução do número de internações pela doença. </p>

