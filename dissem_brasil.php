            <h2 class="text-center">Disseminação do covid no Brasil</h2>
            <p>O Brasil é o único país que, com população superior a 100 milhões, possui um sistema de saúde universal,
                integral e gratuito. Apesar dos cortes no orçamento da saúde, esperava-se que o sistema de saúde do
                Brasil colocasse o país em uma boa posição para mitigar a pandemia COVID-19. No entanto, o Brasil é um
                dos países mais atingidos pela COVID-19. Em 11 de março de 2021, 11.277.717 casos e 272.889 mortes foram
                relatados. Esses representam 9,5% e 10,4% dos casos e óbitos mundiais, respectivamente; no entanto, o
                Brasil compartilha apenas 2,7% da população mundial. No final de maio de 2020, a América Latina foi
                declarada o epicentro da pandemia COVID-19, principalmente por causa do Brasil.</p>
            <p>Sem uma estratégia nacional coordenada, as respostas locais variaram em forma, intensidade, duração e
                horários de início e fim. Após múltiplas introduções do SARS-CoV-2, o Brasil teve uma fase epidêmica
                inicial &#40;15 de fevereiro a 18 de março de 2020&#41; com circulação restrita, precedida por
                circulação viral não detectada. Embora a propagação inicial tenha sido determinada pelas desigualdades
                socioeconômicas existentes, a falta de uma resposta coordenada, eficaz e equitativa provavelmente
                alimentou a propagação espacial generalizada do SARS-CoV-2.</p>
            <p>Em todos os estados, demorou menos de um mês entre o primeiro caso e a primeira morte; apenas 11 dias no
                Amazonas e 21 em São Paulo. À medida que estados e municípios impuseram e amenizaram medidas restritivas
                em diferentes momentos, a mobilidade populacional facilitou a circulação do vírus e atuou como
                desencadeador da disseminação da doença. Em tal cenário, respostas imediatas e equitativas, coordenadas
                em nível federal, são imperativas para evitar a propagação rápida do vírus. No entanto, a resposta do
                COVID-19 no Brasil não foi imediata nem justa.</p>
            <p>Em 11 de março de 2021, o Brasil já registrava 40% do total de mortes de COVID-19 ocorridas em 2020. Em
                janeiro de 2021, Manaus testemunhou um pico de casos e hospitalizações, um colapso do sistema
                hospitalar, incluindo falta de oxigênio para pacientes. O número de mortos é insuportável, pois Manaus
                já registrou 39,8% mais mortes por COVID-19 em 2021 do que em 2020.</p>
